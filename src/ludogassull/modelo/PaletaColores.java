package ludogassull.modelo;

import java.awt.Color;

public class PaletaColores {
    
    private final Color colorRojo = new Color(204,0,0);
    private final Color colorAzul = new Color(0,0,204);
    private final Color colorVerde = new Color(0,153,0);
    private final Color colorOcre = new Color(204,153,0);

    public Color getColorRojo() {
        return colorRojo;
    }

    public Color getColorAzul() {
        return colorAzul;
    }

    public Color getColorVerde() {
        return colorVerde;
    }

    public Color getColorOcre() {
        return colorOcre;
    }
    
}
